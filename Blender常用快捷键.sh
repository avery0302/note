Blender常用快捷键

右键：选中物体
shift+a：添加物体
shift+d：复制物体
x：删除物体
滚轮：缩放世界
按住滚轮拖动：旋转世界
shift+滚轮：移动世界
tab：编辑模式
ctrl+z：撤销
ctrl+shift+z：取消撤销
左键：确认
右键：取消
r：旋转物体
g：移动物体
s：缩放物体
m：移动图层
ctrl：对面的视图
0：主视图
3：右视图
7：俯视图
9：相反视图
t：左工具栏
n：右属性栏
z：查看光照效果

#主要的几个面板
3D视图+物体模式：查看整体效果
3D视图+编辑模式+面选择模式：查看模型各部件的纹理
属性面板+材质：查看一级纹理列表和二级纹理列表
着色器编辑器：查看组成纹理的节点参数