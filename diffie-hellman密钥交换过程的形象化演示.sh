小红和小明想要传递一条消息：下午第1节课一起翘课去网吧
但是担心纸条被老师截获，于是加密了数字信息，于是纸条变成了：下午第4节课一起翘课去网吧（解密方式：数字除以4）
这种方式很傻，因为写在同一张纸条上老师截获还是能够破译，于是他们把纸条分开传递
纸条1：解密方式：数字除以4
纸条2：下午第4节课一起翘课去网吧
但是这种方式还是不保险，因为有可能两张纸条同时被截获，所以他们决定使用diffie-hellman方式对纸条1进行加密
小明想了一个私钥数字4，然后规定了生成元为2，模数为6，计算得到一个公钥结果2^4mod6=4，然后给小红传递消息：生成元2模数6公钥4
小红收到纸条后，给自己想了一个私钥数字3，然后使用模数、小明的公钥和自己的私钥计算出了共享密钥4^3mod6=4
考虑到小明也需要公钥来计算共享密钥，于是小红使用生成元、模数和自己的私钥计算公钥为2^3mod6=2，然后给小明传递消息：公钥2
小明收到后，使用模数、小红的公钥和自己的私钥计算的共享密钥结果是2^4mod6=4，这个结果和小红是一样的
这个过程传递的纸条有两张，
纸条1（小明->小红）：下午第4节课一起翘课去网吧，加密方式diffie-hellman：生成元2模数6公钥4，解密方式：除以密钥数字
纸条2（小红->小明）：公钥2
两个人就都知道了解密后的时间是第一节课，而老师就算截获了这两张纸条，也只能获得一个错误的时间
翘课成功:)