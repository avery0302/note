#sha256是什么
sha256本质是一组0和1数字的组合，长度为256，所以可以表示2^256种不同的组合
可以通过调用api将一整个项目代码、一本小说转成这种01字符串，只要代码或者小说中有一处很小的不同，这个01字符串都会有很大的改变
#sha256的长度为什么是64
因为8位可以叫做一个字节，所以sha256又可以叫做32字节的数据，那么为什么不用32位字符表示sha256呢
因为1字节是8位，代表的种类是256种，但是目前没有256进制的字符表
我们能够使用的最大种类的字符表是16进制，16=2^4，可以唯一区别4位长度的01字符串
所以256位01，每4位为一组，用一个16进制字符来表示，最后结果就是长度为64的16进制字符串
#位数在编码中的应用
utf-8规范：
2^8=256，8位可以表示大小写英文字母和数字
2^16=65536，16位可以表示拉丁字符（端口范围也是用16位表示，所以是0-65535）
2^24=1677216，24位可以表示汉字
2^32=4294967296，32位可以表示emoji表情
#sha256的使用场景
sha256可以用来标识代码是否改变，比如github上的每次提交都会附一个sha256，理论上任何两次的提交sha256都是不同的
大部分情况下前7位都不会有相同的，因为代码的改变，提交时间的改变，提交信息的改变都会影响这个sha256值
另外docker镜像的那串代码，也是sha256
#sha256和sha1的区别
sha1代指sha160，有2^160种排列，远少于2^256种，现已被证明不够安全而弃用