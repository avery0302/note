使用对应字符的unicode码点即可
#常见对应关系
&#x3C;  (表示 <)
&#x3E;  (表示 >)
&#x22;  (表示 ")
&#x27;  (表示 ')
&#x68;&#x74;&#x74;&#x70;&#x73;&#x3A;&#x2F;&#x2F;  (表示 https://)
&#x5FAE;&#x4FE1;  (表示 微信)
&#x3C;img src="x" onerror="alert(250)"&#x3E;  (表示<img src="x" onerror="alert(250)">)
&#x3C;script&#x3E;alert(250)&#x3C;/script&#x3E;  (表示<script>alert(250)</script>)